import java.util.*;
public class reverseString{
    public static void main(String args[]){
        String s="yash verma";
        String rev="";
        for(int j=s.length()-1;j>=0;j--){
            rev+=s.charAt(j);
        }
        System.out.println(rev);
    }
}


